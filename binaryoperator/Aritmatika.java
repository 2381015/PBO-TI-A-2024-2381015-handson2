package binaryoperator;

public class Aritmatika {
    public static void main(String[] args) {
        int operand1 = 20;
        int operand2 = 5;

        System.out.println(operand1 + operand2);//25
        System.out.println(operand1 - operand2);//15
        System.out.println(operand1 * operand2);//100
        System.out.println(operand1 / operand2);//4
        System.out.println(operand1 % operand2);//0
    }
}
